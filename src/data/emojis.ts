// Flatten all emojis for search
export const allEmojis = emojiCategories.flatMap(cat => cat.emojis);

// Get random emojis
export const getRandomEmojis = (count: number): string[] => {
  const shuffled = [...allEmojis].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

// Search emojis
export const searchEmojis = (query: string): string[] => {
  if (!query) return allEmojis.slice(0, 50);
  // Simple search - in real app would search by name
  return allEmojis.slice(0, 30);
};
